package Methods;

public class Calculator 
{
	void add(int a , int b)
	{
		int c = a+b;
		System.out.println("Addition ="+c);
	}
	
	float div(float a, float b)
	{
		return a/b;
	}
	
	int multiplication(int a, int b)
	{
		return a*b;
	}
	
	double sub(double a , double b)
	{
		return a-b;
	}
	
	String concate(String s1, String s2)
	{
		return s1+s2;
	}

}
